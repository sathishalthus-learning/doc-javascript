// typed arrays
// typed arrays are array-like objects that provide a mechanism for reading and writing raw binary data in memory buffers
// they are used to work with binary data directly
// typed arrays are available in the global scope

// creating a typed array
// syntax: new TypedArray(length);
// TypedArray is one of the typed array types like Int8Array, Uint8Array, Int16Array, Uint16Array, Int32Array, Uint32Array, Float32Array, Float64Array
// length is the number of elements in the array
// the array is initialized with zeros
